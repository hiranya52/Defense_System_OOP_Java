    @Override
    public void areaCleared(boolean areaClear) {
        if (areaClear) {
            lblAreaClear.setOpaque(true);
            lblAreaClear.setText("Area Is Cleared");
            lblAreaClear.setBackground(Color.GREEN);
        } else {
            lblAreaClear.setOpaque(true);
            lblAreaClear.setText("Area Is Not Cleared");
            lblAreaClear.setBackground(Color.RED);
        }
    }

    @Override
    public void txtSender(String msg) {
        txtMsgBox.append("Main Controller : " + msg+"\n");
    }

    @Override
    public void defenceUnloker(int value) {
        if (chkPosition.isSelected()) {
            if (value >= 20) {
                btnShoot.setEnabled(true);
            } else {
                btnShoot.setEnabled(false);
            }
            if (value >= 40) {
                btnSonarOperation.setEnabled(true);
            } else {
                btnSonarOperation.setEnabled(false);
            }
            if (value >= 60) {
                btnToahawkMissile.setEnabled(true);
            } else {
                btnToahawkMissile.setEnabled(false);
            }
            if (value >= 80) {
                btnTridentMissile.setEnabled(true);
            } else {
                btnTridentMissile.setEnabled(false);
            }
        } else{
            btnShoot.setEnabled(false);
            btnSonarOperation.setEnabled(false);
            btnToahawkMissile.setEnabled(false);
            btnTridentMissile.setEnabled(false);
        }
    }

